package PricesExample;

public class Data {
    String idOfCoin;
    String nameOfCoin;
    String currentPrice;
    public String getIdOfCoin() {
        return idOfCoin;
    }
    public void setIdOfCoin(String idOfCoin) {
        this.idOfCoin = idOfCoin;
    }
    public String getNameOfCoin() {
        return nameOfCoin;
    }
    public void setNameOfCoin(String nameOfCoin) {
        this.nameOfCoin = nameOfCoin;
    }
    public String getCurrentPrice() {
        return currentPrice;
    }
    public void setCurrentPrice(String currentPrice) {
        this.currentPrice = currentPrice;
    }
    

}
