package huster.crawl;

public class ItemWebs {
    private String url;
    private String domain;
    private String titleTag;
    private String authorTag;
    private String dateTag;
    private String categoryTag;
    private String summaryTag;
    private String mainTag;
    private String linkTag;
    private String hashTag;

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getDomain() {
        return domain;
    }

    public void setDomain(String domain) {
        this.domain = domain;
    }

    public String getTitleTag() {
        return titleTag;
    }

    public void setTitleTag(String titleTag) {
        this.titleTag = titleTag;
    }

    public String getAuthorTag() {
        return authorTag;
    }

    public void setAuthorTag(String authorTag) {
        this.authorTag = authorTag;
    }

    public String getDateTag() {
        return dateTag;
    }

    public void setDateTag(String dateTag) {
        this.dateTag = dateTag;
    }

    public String getCategoryTag() {
        return categoryTag;
    }

    public void setCategoryTag(String categoryTag) {
        this.categoryTag = categoryTag;
    }

    public String getSummaryTag() {
        return summaryTag;
    }

    public void setSummaryTag(String summaryTag) {
        this.summaryTag = summaryTag;
    }

    public String getMainTag() {
        return mainTag;
    }

    public void setMainTag(String mainTag) {
        this.mainTag = mainTag;
    }

    public String getLinkTag() {
        return linkTag;
    }

    public void setLinkTag(String linkTag) {
        this.linkTag = linkTag;
    }

    public String getHashTag() {
        return hashTag;
    }

    public void setHashTag(String hashTag) {
        this.hashTag = hashTag;
    }

}
